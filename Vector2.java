public class Vector2 {
    private int x;
    private int y;

    public Vector2() {
        x = 0;
        y = 0;
    }
    
    public Vector2(int xIn, int yIn) {
        x = xIn;
        y = yIn;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}